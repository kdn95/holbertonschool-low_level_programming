#include "3-calc.h"
/**
 * op_add - addition
 * @a: first int
 * @b: second int
 * Return: result
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 *op_sub - subtract
 * @a: first int
 * @b: second int
 * Return: results
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplication
 * @a: first int
 * @b: second int
 * Return: results
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divide
 * @a: first int
 * @b: second int
 * Return: results
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modular num
 * @a: first int
 * @b: second int
 * Return: results
 */
int op_mod(int a, int b)
{
return (a % b);
}
